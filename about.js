/*
    ## What is Node.js

    Node.js = Run JavaScript everywhere.

    Remember, wherever there is JavaScript, there is always a JavaScript engine.
    Without it you can’t run JavaScript code. Every browser has its own JavaScript engine —
    Firefox has SpiderMonkey, Google Chrome has the V8 engine.

    Node.js is a JavaScript runtime (runtime environment) built on top of the JavaScript V8 engine.
    Later on the V8 engine became very popular, so Node.js gets its power from V8.
    We can say Node.js runs on the V8 JS engine and executes JavaScript code outside the web browser.

    Node.js is cross-platform. Meaning you can run it on Windows, Linux, macOS —
    basically on almost all platforms.

    Node.js is maintained by the committee of the OpenJS Foundation.
    They add code to it, update it, and maintain it.

    Node.js has an event-driven architecture and supports asynchronous I/O (non-blocking I/O).

    Servers like Apache are blocking servers. With Node.js, developers wanted
    to solve the blocking issue and make it non-blocking so we can handle multiple
    requests with fewer threads.

    ---------------------------------------------------------------
    ## Node.js – JavaScript on the Server

    Node.js gives us the ability to run JavaScript outside the browser, primarily on servers.

    A server is simply a computer (CPU). When we host a website on a server and we access it,
    there is a machine somewhere receiving our request. For example, if you type "google.com",
    your computer sends a request to the Google server. That server receives the request,
    processes it, and sends back a response.

    Node.js allows us to write the logic of that server using JavaScript.

    ---------------------------------------------------------------
    ## JavaScript Engines (Important)

    All JavaScript engines must follow ECMAScript (ES) standards so that JavaScript behaves
    the same everywhere.

    JavaScript engines themselves are written in C++, and they execute JavaScript code
    on top of that C++ layer. The V8 engine (from Chrome) can even be embedded inside
    any C++ application.

    Node.js is basically a C++ program that embeds the V8 engine. Because of this,
    Node.js can execute JavaScript outside the browser, handle server tasks,
    read files, interact with networks, and much more.
*/

